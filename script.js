// Ocultar loader y mostrar contenido cuando la página carga completamente
window.addEventListener('load', function() {
    document.body.classList.add('loaded');
    document.getElementById('loader').style.display = 'none';
    document.getElementById('content').style.display = 'block';
});

// Mostrar el nombre del estudiante automáticamente al ingresar el DNI
document.getElementById('dni').addEventListener('input', function(event) {
    const dniInput = event.target.value;
    const nameField = document.getElementById('name');
    
    if (dniList[dniInput]) {
        nameField.value = dniList[dniInput];
    } else {
        nameField.value = ""; // Limpiar si el DNI no está en la lista
    }
});

// Manejar el envío del formulario de votación
document.getElementById('votingForm').addEventListener('submit', function(event) {
    event.preventDefault();
    
    const dni = document.getElementById('dni').value;
    const name = document.getElementById('name').value;
    const vote = document.querySelector('input[name="vote"]:checked').value;

    if (validateDNI(dni) && name.trim() !== "" && !isDNIUsed(dni)) {
        saveVote(dni, name, vote);
    } else if (isDNIUsed(dni)) {
        document.getElementById('result').innerText = 'Este DNI ya ha sido utilizado para votar.';
    } else {
        document.getElementById('result').innerText = 'Por favor, ingrese un DNI válido y su nombre.';
    }
});

function validateDNI(dni) {
    return dniList.hasOwnProperty(dni);
}

function isDNIUsed(dni) {
    let votes = JSON.parse(localStorage.getItem('votes')) || [];
    return votes.some(vote => vote.dni === dni);
}

function saveVote(dni, name, vote) {
    const voteData = { dni, name, vote };
    let votes = JSON.parse(localStorage.getItem('votes')) || [];
    votes.push(voteData);
    localStorage.setItem('votes', JSON.stringify(votes));
    document.getElementById('result').innerText = `¡Gracias, ${name}! Tu voto para "${vote}" ha sido registrado.`;
}

function checkPassword() {
    const inputPassword = document.getElementById('adminPassword').value;

    if (inputPassword === adminPassword) {
        showVotes();
    } else {
        document.getElementById('result').innerText = 'Contraseña incorrecta.';
    }
}

function showVotes() {
    let votes = JSON.parse(localStorage.getItem('votes')) || [];
    let option1Count = 0;
    let option2Count = 0;
    let option3Count = 0;

    votes.forEach(vote => {
        if (vote.vote === "Opción 1") {
            option1Count++;
        } else if (vote.vote === "Opción 2") {
            option2Count++;
        } else if (vote.vote === "Opción 3") {
            option3Count++;
        }
    });

    document.getElementById('result').innerHTML = `
        <h3>Resultados Totales:</h3>
        <p>Opción 1: ${option1Count} votos</p>
        <p>Opción 2: ${option2Count} votos</p>
        <p>Opción 3: ${option3Count} votos</p>
    `;
}
